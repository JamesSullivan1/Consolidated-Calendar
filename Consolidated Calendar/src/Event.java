
public class Event {
	private String name;
	private String location;
	private String startDate;
	private String endDate;
	
	public void Event(){
		
	}
	public String getName(){
		return name;
	}
	
	public String getLocation(){
		return location;
	}
	public String getStart(){
		return startDate;
	}
	public String getEnd(){
		return endDate;
	}
	public String toString(){
		return name;
	}
}
